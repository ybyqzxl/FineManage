public class OrderAndScore {


    /**
     * catename : 理想信念
     * deporder : 1
     * claorder : 1
     * score : 0
     */

    private String catename;
    private String deporder;
    private String claorder;
    private String score;

    public String getCatename() {
        return catename;
    }

    public void setCatename(String catename) {
        this.catename = catename;
    }

    public String getDeporder() {
        return deporder;
    }

    public void setDeporder(String deporder) {
        this.deporder = deporder;
    }

    public String getClaorder() {
        return claorder;
    }

    public void setClaorder(String claorder) {
        this.claorder = claorder;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }
}
